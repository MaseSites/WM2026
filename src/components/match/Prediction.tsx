import type { Match } from "@/lib/types";
import { team } from "@/lib/data";
import { Ring } from "@/components/ui/Ring";
import { ProbBar } from "@/components/charts/ProbBar";
import { Icon } from "@/components/ui/Icon";

function Metric({ label, value, hint }: { label: string; value: string; hint?: string }) {
  return (
    <div className="rounded-lg border border-line bg-white/[0.02] p-3">
      <div className="text-[10px] font-medium uppercase tracking-wider text-ink-3">{label}</div>
      <div className="mt-1 text-[15px] font-semibold">{value}</div>
      {hint && <div className="text-[10.5px] text-ink-3">{hint}</div>}
    </div>
  );
}

export function Prediction({ m }: { m: Match }) {
  const p = m.prediction;
  if (!p) return null;
  const h = team(m.homeId);
  const a = team(m.awayId);

  return (
    <div className="panel overflow-hidden">
      <div className="flex items-center justify-between border-b border-line px-4 py-3 sm:px-5">
        <div className="flex items-center gap-2">
          <span className="grid h-7 w-7 place-items-center rounded-md bg-accent/12 ring-1 ring-accent/25">
            <Icon name="Sparkles" className="h-4 w-4 text-accent" />
          </span>
          <div>
            <h3 className="text-[14px] font-semibold">WorldMap AI analysis</h3>
            <p className="text-[11px] text-ink-3">Model v4 · 12,000 simulations</p>
          </div>
        </div>
        <div className="flex items-center gap-2">
          <Ring value={p.confidence} size={52} stroke={5} sublabel="conf." />
        </div>
      </div>

      <div className="space-y-5 p-4 sm:p-5">
        <ProbBar
          home={p.homeWin}
          draw={p.draw}
          away={p.awayWin}
          homeLabel={`${h.short} win`}
          awayLabel={`${a.short} win`}
        />

        <div className="grid grid-cols-2 gap-2.5 sm:grid-cols-4">
          <Metric label="Likely score" value={p.likelyScore} />
          <Metric label="Expected goals" value={`${p.expectedGoals[0]} – ${p.expectedGoals[1]}`} />
          <Metric label="Likely scorer" value={p.likelyScorer} />
          <Metric label="First goal" value={p.firstGoal} />
          <Metric label="Upset chance" value={`${p.upset}%`} hint="model tail risk" />
          <Metric label="Penalty" value={`${p.penalty}%`} hint="spot-kick in match" />
          <Metric label="Clean sheet" value={`${p.cleanSheet[0]}% / ${p.cleanSheet[1]}%`} hint={`${h.short} / ${a.short}`} />
          <Metric label="Most dangerous" value={p.mostDangerous} />
        </div>

        <div>
          <div className="mb-2 flex items-center gap-2 text-[11px] font-semibold uppercase tracking-[0.14em] text-ink-3">
            <Icon name="Zap" className="h-3.5 w-3.5 text-accent" />
            Why the model leans this way
          </div>
          <ul className="space-y-2.5">
            {p.narrative.map((line, i) => (
              <li key={i} className="flex gap-2.5 text-[13px] leading-relaxed text-ink-2">
                <span className="mt-1.5 h-1.5 w-1.5 shrink-0 rounded-full bg-accent/70" />
                {line}
              </li>
            ))}
          </ul>
        </div>

        <p className="rounded-lg bg-white/[0.02] px-3 py-2 text-[11px] leading-relaxed text-ink-3">
          Generated by WorldMap AI from form, xG, squad availability and 20 years of World Cup
          data. For entertainment — not betting advice.
        </p>
      </div>
    </div>
  );
}
